namespace WheatTrace.Domain.Enums;

/// <summary>
/// حالة الانتداب: تتبّع مسار التكليف منذ إنشائه حتى انتهائه.
/// Active   → الانتداب جارٍ حالياً والمفتش مخصَّص لهذا الموقع
/// Replaced → تم استبداله بانتداب آخر (الأرشفة دون حذف)
/// Inactive → انتهى الانتداب دون استبدال (نهاية موسم أو إلغاء)
/// </summary>
public enum AssignmentStatus
{
    Active,    // نشط - المفتش مخصَّص حالياً للموقع
    Replaced,  // مُستبدَل - تم استبداله تلقائياً بانتداب جديد
    Inactive   // منتهٍ - أُلغي أو انتهت مدته دون استبدال
}
