namespace WheatTrace.Domain.Enums;

/// <summary>
/// حالة الموقع التشغيلية: تُشتَق من آخر حدث في سجل دورة الحياة.
/// Active    → مفتوح ويستقبل الشاحنات حالياً
/// Closed    → مغلق نهائياً أو بشكل مؤقت
/// Full      → وصل للطاقة الاستيعابية القصوى ولا يستقبل المزيد
/// Suspended → مُعلَّق مؤقتاً (صيانة أو ظروف طارئة)
/// </summary>
public enum SiteStatus
{
    Active,    // نشط - يستقبل القمح
    Closed,    // مغلق
    Full,      // ممتلئ - وصل للطاقة الاستيعابية
    Suspended  // مُعلَّق مؤقتاً
}
