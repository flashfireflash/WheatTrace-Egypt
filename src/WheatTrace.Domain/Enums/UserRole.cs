namespace WheatTrace.Domain.Enums;

/// <summary>
/// أدوار المستخدمين في المنظومة - يُحدد هذا الدور الصلاحيات في كل مكان.
/// الأدوار مرتبة هرمياً من الأوسع صلاحيةً إلى الأضيق:
///
/// Admin             → مدير النظام: صلاحية كاملة على جميع البيانات والإعدادات
/// GeneralMonitor    → المراقب العام: يرى كل شيء على مستوى الجمهورية
/// OperationsMonitor → مراقب العمليات: يتابع العمليات اليومية ويتواصل مع المفتشين
/// GovernorateManager→ مسؤول المحافظة: يدير مواقع وانتدابات محافظته فقط
/// Inspector         → المفتش الميداني: يُدخل بيانات التوريد اليومية لموقعه
/// </summary>
public enum UserRole
{
    Admin,              // مدير النظام
    GeneralMonitor,     // المراقب العام
    OperationsMonitor,  // مراقب العمليات
    GovernorateManager, // مسؤول المحافظة
    Inspector,          // المفتش الميداني
    SuperAdmin          // (مخصص للنظام - لا يُذكر في التوثيق العام)
}
